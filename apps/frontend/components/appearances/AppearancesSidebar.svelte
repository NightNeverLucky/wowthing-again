<script lang="ts">
    import sortBy from 'lodash/sortBy'

    import { expansionMap } from '@/data/expansion'
    import { appearanceStore } from '@/stores'
    import type { SidebarItem, UserCount } from '@/types'

    import ProgressBar from '@/components/common/ProgressBar.svelte'
    import Sidebar from '@/components/sub-sidebar/SubSidebar.svelte'
    import { weaponSubclassOrder, weaponSubclassToString } from '@/data/weapons'

    let categories: SidebarItem[] = []
    let stats: UserCount
    $: {
        categories = [
            {
                name: 'Expansion',
                slug: 'expansion',
                children: sortBy(
                    Object.values(expansionMap),
                    (expansion) => 100 - expansion.id
                ),
            },
            null,
            {
                name: 'Cloth',
                slug: 'cloth',
                children: slots,
            },
            {
                name: 'Leather',
                slug: 'leather',
                children: slots,
            },
            {
                name: 'Mail',
                slug: 'mail',
                children: slots,
            },
            {
                name: 'Plate',
                slug: 'plate',
                children: slots,
            },
            null,
            {
                name: 'Weapons',
                slug: 'weapons',
                children: weaponChildren,
            }
        ]
        stats = $appearanceStore.data.stats['OVERALL']
    }

    const percentFunc = function(entry: SidebarItem, parentEntries?: SidebarItem[]) {
        if (parentEntries?.length < 1 && entry.name === 'Expansion') {
            return -1
        }

        const slug = [...parentEntries, entry].slice(-2)
            .map((entry) => entry.slug)
            .join('--')
        const hasData = $appearanceStore.data.stats[slug]
        return (hasData?.have ?? 0) / (hasData?.total ?? 1) * 100
    }

    const slots: SidebarItem[] = [
        {
            name: 'Head',
            slug: 'head',
        },
        {
            name: 'Shoulders',
            slug: 'shoulders',
        },
        {
            name: 'Chest',
            slug: 'chest',
        },
        {
            name: 'Wrist',
            slug: 'wrist',
        },
        {
            name: 'Hands',
            slug: 'hands',
        },
        {
            name: 'Waist',
            slug: 'waist',
        },
        {
            name: 'Legs',
            slug: 'legs',
        },
        {
            name: 'Feet',
            slug: 'feet',
        },
    ]

    const weaponChildren: SidebarItem[] = weaponSubclassOrder
        .map((subClass) => ({
            name: weaponSubclassToString[subClass],
            slug: weaponSubclassToString[subClass].toLowerCase().replace(/ /g, '-'),
        }))
</script>

<style lang="scss">
    div {
        margin-bottom: 0.75rem;
    }
</style>

<Sidebar
    baseUrl="/appearances"
    items={categories}
    noVisitRoot={true}
    width="16rem"
    {percentFunc}
>
    <div slot="before">
        <ProgressBar
            title="Overall"
            have={stats.have}
            total={stats.total}
        />
    </div>
</Sidebar>
